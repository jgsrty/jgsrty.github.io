<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>As food is to the body, so is learning to the mind.</p>
<p>Our bodies grow and muscles develop with the intake of adequate nutritious food.</p>
<p>Likewise, we should keep learning day by day to maintain our keen mental power and expand our intellectual capacity.</p>
<p>Constant learning supplies us with inexhaustible fuel for driving us to sharpen our power of reasoning, analysis, and judgment.</p>
<p>Learning incessantly is the surest way to keep pace with the times in the information age, and an infallible warrant of success in times of uncertainty.</p>
<p>Once learning stops, vegetation sets in.</p>
<p>It is a common fallacy to regard school as the only workshop for the acquisition of knowledge.</p>
<p>On the contrary, learning should be a never-ending process, from the cradle to the grave.</p>
<p>With the world ever changing so fast, the cease from learning for just a few days will make a person lag behind.</p>
<p>What's worse, the animalistic instinct dormant deep in our subconsciousness will come to life, weakening our will to pursue our noble ideal, sapping our determination to sweep away obstacles to our success and strangling for the refinement of our character.</p>
<p>Lack of learning will inevitably lead to the stagnation of the mind, or even worse, its fossilization.</p>
<p>Therefore, to stay mentally young, we have to take learning as a lifelong career.</p>
