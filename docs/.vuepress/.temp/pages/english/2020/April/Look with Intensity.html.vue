<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>It seems to me that learning is astonishingly diffcult, as is listening also.</p>
<p>We never actually listen to anything because our mind is not free, our ears are stuffed up with those things that we already know, so listening becomes extraordinarily difficult.</p>
<p>I think -- or rather, it is a fact -- that if one can listen to something with all of one's being, with vigor, with vitality, then the very act of listening is a liberative factor, but unfortunately you never do listen, as you have never learned about it.</p>
<p>After all, you only learn when you give your whole being to something.</p>
<p>When you give your whole being to mathematics, you learn.</p>
<p>But when you are in a state of contradiction, when you do not want to learn but are forced to learn, then it become merely a process of accumulation.</p>
<p>To learn is like reading a novel with innumerable characters, it requires your full attention, not contradictory attention.</p>
<p>If you want to learn about a leaf -- a leaf of the spring or a leaf of summer -- you must really look at it, see the symmetry of it, the texture of it, the quality of the living leaf.</p>
<p>There is beauty, there is vigor, there is vitality in a single leaf.</p>
<p>So to learn about the leaf, the flower, the cloud, the sunset, or a human being, you must look with all intensity.</p>
