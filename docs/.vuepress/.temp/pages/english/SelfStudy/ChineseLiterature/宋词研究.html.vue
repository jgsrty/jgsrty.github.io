<h3 id="苏轼与元祐词人" tabindex="-1"><a class="header-anchor" href="#苏轼与元祐词人" aria-hidden="true">#</a> 苏轼与元祐词人</h3>
<ol>
<li>苏轼 宋词繁荣期 《东坡乐府》
词创作起步较晚，大家特点气魄，中心：以诗为词</li>
</ol>
<ul>
<li>苏轼的诗词文书画皆是大家，强调各种文艺门类精神上的相通，不拘泥于各自特点，所以其词不为传统范围所限。</li>
<li>“自是一家”的自觉创新意识，自创一体，开拓创新词境</li>
<li>“自是一家”的正面主张：使词从传统女性题材中跳出来，充满博大情怀，强调阳刚之美。</li>
<li>创新归结为以诗为词，凡可入诗亦可入词，又并不主张诗词不分，强调“同质”又充分注意“异体”</li>
</ul>
<ol start="2">
<li>黄庭坚 苏门四学士</li>
</ol>
<ul>
<li>词学观：从本体论延伸到作家论、创作论。主张提高创作主体的学养、人格境界。词风与苏轼接近。词的框架主体塑造了士大夫自我人格形象。</li>
<li>词留180余首，多言志抒怀之作。意境刚大之气，表现出自我个体意识。</li>
</ul>
