<h2 id="词调三类-词的音乐分类概念" tabindex="-1"><a class="header-anchor" href="#词调三类-词的音乐分类概念" aria-hidden="true">#</a> 词调三类（词的音乐分类概念）</h2>
<ol>
<li>令、引、近、慢</li>
</ol>
<ul>
<li>令：又称小令，唐人宴席间所行的酒令</li>
<li>引：引本是古代音乐的一种名称，在曲中有前奏、序曲的意思</li>
<li>近：又称近拍、近拍为全称，近是近拍的省称</li>
<li>慢：慢是慢曲子的简称，慢词一般字多调长</li>
</ul>
<ol start="2">
<li>偷声、减字</li>
</ol>
<ul>
<li>偷声与减字意义相同，在音乐上节短乐句简化节奏，在歌辞上减少字句</li>
</ul>
<ol start="3">
<li>添声、添字、摊声、摊破</li>
</ol>
<ul>
<li>与偷声减字相反，添声、添字、摊声、摊破，都是在音乐上添入乐句加繁节奏，在歌辞上增多字句，四者意义相同</li>
</ul>
<h2 id="词艺" tabindex="-1"><a class="header-anchor" href="#词艺" aria-hidden="true">#</a> 词艺</h2>
<ol>
<li>
<p>起节：开头、结尾。草调、双调、三叠、四叠。<br>
一般以双调为准。上篇结尾也叫结，上篇最后一句叫歇拍，双调最后一句叫结拍，既是下篇的结也是全篇的结。</p>
</li>
<li>
<p>离合：词艺术传达方面的一种技法。<br>
咏物词体现最清楚，要求：不即不离，若即若离，能即能离</p>
</li>
<li>
<p>点染：本是中国画技法名称，指词中勾勒与晕染并用的现象。<br>
点：点示，醒明情谊。<br>
染：渲染，具体描绘。<br>
点染要相辅相成的运用</p>
</li>
</ol>
<h4 id="怀古词的审美层次" tabindex="-1"><a class="header-anchor" href="#怀古词的审美层次" aria-hidden="true">#</a> 怀古词的审美层次</h4>
<ol>
<li>类型化的情绪和感知方式。这种感伤氛围及凭吊之情比较空泛甚至模式化。</li>
<li>对这种感知方式的突破。指融进了作者切实生活感受，具有强烈个人意识。</li>
<li>强化了怀古主题中独立的个体意识，借怀古感世来传达个人关于历史人生价值的评判。</li>
</ol>
<h4 id="咏物词的审美层次-物本位、我本位、物我同一" tabindex="-1"><a class="header-anchor" href="#咏物词的审美层次-物本位、我本位、物我同一" aria-hidden="true">#</a> 咏物词的审美层次 物本位、我本位、物我同一</h4>
<ol>
<li>物本位：以形容尽致为审美目的，偏重对外物客观性特征的再现，审美意象具有写实性、如画性。</li>
<li>我本位：以我观物，主体是我，客体是主体的奴仆。出发点是确定的意念，到自然中寻找表现客观对应物。</li>
<li>物我同一： 亦物亦我，身与物化。心物同构感应的现象。</li>
</ol>
