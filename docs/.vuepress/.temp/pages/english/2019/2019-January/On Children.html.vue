<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>And a woman who held a babe against her bosom said, speak to us of Children.</p>
<p>And he said:</p>
<p>Your Children are not your children.</p>
<p>They are the sons and daughters of Life's longing for itself.</p>
<p>They come through you but not from you, and though they are with you yet they belong not to you.</p>
<p>You may give them your love but not your thoughts, for they have their own thoughts.</p>
<p>You may house their bodies but not their souls, for their souls dwell in the house of tomorrow, which you cannot visit, not even in your dreams.</p>
<p>You may strive to be like them, but seek not to make them like you.</p>
<p>For life gose not backward nor tarries with yesterday.</p>
<p>You are the bows from which your children as living arrows are sent forth.</p>
<p>The archer sees the mark upon the path of the infinite, and he bends you with his might that his arrows may go swift and far.</p>
<p>Let your bending in the archer's hand be for gladness, for even as he loves the arrow that flies, so he loves also the bow that is stable.</p>
<blockquote>
<p>纪·哈·纪伯伦（Kahlil Gibran），黎巴嫩诗人、画家、被称为‘体术天才’、‘黎巴嫩文坛骄子’，代表作品有《我的心灵告诫我》《先知》《论友谊》等。纪伯伦是阿拉伯文学的主要奠基人，爱与美是其作品的主旋律。</p>
</blockquote>
