<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>Water is virtually everythere, from soil moisture and ice caps, to the cells inside our own bodies.</p>
<p>Depending on factors like location, fat index, age, and sex, the average human is between 55-65% water.</p>
<p>At birth, human babies are even wetter. Being 75% water, they are swimmingly similar to fish.</p>
<p>But their water composition drops to 65% by their first birthday.</p>
<p>So what role dose water play in our bodies, and how much do we actually need to drink to stay healthy?</p>
<p>The H2O in our bodies works to cushion and lubricate joints, regulate temperature, and to nourish the brain and spinal cord.</p>
<p>Water isn't only in our blood.</p>
<p>An adult's brain and heart are almost three quarters water. That's roughly equivalent to the amount of moisture in a banana.</p>
<p>Lungs are more similar to an apple at 83%. And even seemingly dry human bones are 31% water.</p>
<p>If we essentially made of water, and surrounded by water, why do we need to drink so much?</p>
<p>Well, each day we lose two to three liters through our sweat, urine, and bowel movements, and even just from breathing.</p>
<p>While these functions are essential to our survival, we need to compensate for the fluid loss.</p>
<p>Maintaining a balanced water level is essential to avoid dehydration or over-hydration, both of which can have devastating effects on overall health.</p>
