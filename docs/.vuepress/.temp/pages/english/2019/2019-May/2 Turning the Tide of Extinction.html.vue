<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>Today we face an unprecedented surge in the brutal slaughter of iconic animals by poachers.</p>
<p>In South Africa, for example, one rhino was killed every month until 2005.</p>
<p>But last year, three rhinos were killed every single day.</p>
<p>In the 33 years since I was born, we have lost around 70% of Africa's elephant population.</p>
<p>Of those that are left, 20,000 are being killed every year.</p>
<p>That is 54 elephants killed every single day.</p>
<p>At this rate, children born this year, like my daughter Charlotte, will see the last wild elephants and rhinos die before their 25th birthdays.</p>
<p>Those who suffer the most from this loss are some of the poorest people on our planet.</p>
<p>They are the families who feel powerless as wildlife around them disapper, who face being trapped in poverty forever, without the income that should be brought in by tourism, a cornerstone of the economy in many developing nations.</p>
<p>They are the children whose parents risk their lives in the fight against poachers.</p>
<p>In the last few days, three rangers and one member of the armed forces were killed by poachers in one incident in Central Africa, leaving behind 14 children between them.</p>
<p>It is these children's future that is blighted so tragically by the illegal wildlife trade, and it is their birthright that is stolen.</p>
<p>There is no hiding from these facts today.</p>
<p>On our phones, laptops and our TV screens, we can see the images and read the reports that lay bare the truth of this crisis.</p>
<p>That knowledge brings responsibility: the responsibility to do every thing in our power to reverse the march towards eradications of these fine animals.</p>
<p>The good news is that we are far from powerless in this struggle.</p>
<p>We can turn the tide of extinction.</p>
<p>We know where the animals we are trying to protect live.</p>
<p>We know many of the roads, the airports and ports criminals use to transfer their cargo, from killed fields to marketplace.</p>
