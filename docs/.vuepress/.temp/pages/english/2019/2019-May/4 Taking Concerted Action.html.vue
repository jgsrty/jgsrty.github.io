<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>I do not think that anyone would stand and watch an elephant or rhino being killed.</p>
<p>Or a ranger being gunned down because we wanted a bracelet or an ornament to impress someone else as a gift.</p>
<p>But that is what the demand for wildlife products means in practice.</p>
<p>The decisions we make as consumers affect the lives of ordinary people thousands and thousands of miles away, in countries we may never visit.</p>
<p>If we buy illegal wildlife products, we are contributing to the extinction of whole species.</p>
<p>But there is good news, and if you remember one thing, I want you to remember this: We can win the battle. Each generation decides what it values.</p>
<p>Each generation can determine what we consider to be beautiful on the one hand, or unacceptable, or immoral on the other.</p>
<p>We can act in solidarity with those fighting poaching and trafficking in their communities.</p>
<p>I am absolutely convinced that China can become a global leader in the protection of wildlife.</p>
<p>Your influence in the world means you can change the face of conservation in this century.</p>
<p>This will be a contribution that would go down in history, one that your great grandchildren would speak of with great pride.</p>
<p>The greatest inheritance we can pass on to the next generation is a safe and sustainable environment, the priceless endowment of nature.</p>
<p>Let us not tell our children the sad tale of how we watched as the last elephants, rhinos and tigers died out, but the inspiring story of how we turned the tide and preserved them for all humanity.</p>
<p>And in so doing, let us show the world that by working together we can stand up to the great challenges our planet and our familys will face in the generations to come.</p>
