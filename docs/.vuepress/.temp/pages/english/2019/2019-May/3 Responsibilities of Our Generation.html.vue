<h2 id="原文阅读" tabindex="-1"><a class="header-anchor" href="#原文阅读" aria-hidden="true">#</a> 原文阅读</h2>
<p>And over the last few years, we have seen a groundswell of action by goverments to improve their laws and to work across borders to fight the traffickers.</p>
<p>Only last month, President Xi announced that China would take steps to halt the domestic trade in ivory, adding to the ban on ivory carving imports he announced in Feburary.</p>
<p>But we know the illegal wildlife trade cannot be solved by governments alone.</p>
<p>The spotlight falls back on all of us, and on the choices we have to make to play our parts in addressing this problem.</p>
<p>We have to accept the truth: that consumers are driving the demand for animal body parts -- for arts, for trinkets, or for medicine.</p>
<p>Only we as consumers can put the wildlife traffickers out of business by ending our demand for their products. I know we can do this.</p>
<p>The desire to possess animal trophies or ornaments made from ivory has been felt on every continent for centuries.</p>
<p>I know this topic is sensitive for many families.</p>
<p>For example, until a hundred years ago, my ancestors were those who had little concern about acquiring ivory without knowledge of the threats of extinction, corruption and violence that the ivory trade would lead to.</p>
<p>My rejection of ivory today is not a judgment of past generations.</p>
<p>It is an acceptance of the world as I find it today, and the world that I want my children, George and Charitte, to inherit.</p>
<p>Likewise, those doctors and medical practitioners in China who are speaking out against endangered species in medicines.</p>
<p>They are not judging previous generations who did not have the facts that you do today.</p>
<p>They are just accepting the truth: that all credible evidence and scientific research shows, for example, the rhino horn cannot cure cancer.</p>
<p>We have a responsibility to act on the facts that we have today.</p>
<p>By doing so, we are honoring the generations that have come before us and we are protecting those that are yet to come.</p>
