<blockquote>
<p>Chris Medina曾经是星巴克的服务员。2007年12月12日，他向和他相恋8年的女孩Juliana提出订婚。虽然只是订婚，但Chris Median后来在《美国偶像》的比赛采访中说过：“We kind of make it a promise to get married.”把它（订婚）作为结婚的一个承诺。2009年10月2日，订婚后两年，那个美丽的女孩因为车祸事故，脑部受到重创，康复后智力只有2岁，生活完全不能够自理。但Chris不离不弃。 后来Chris参加了美国偶像，为的就是赚到更多的钱让未婚妻受到更好的治疗过上更好的生活。虽然最后还是止步于全国24强，但 What are words 这首充满爱的歌已经牢牢地印在人们的心里。有一种感情，能让旁观者为之动容，那就是无私的爱。听着他的歌你能感受到他有多爱他的妻子，他遵守着自己的承诺，不管爱的人现在如何，她永远都是他的天使！<br>
二人已经于2014年正式分手，Medina永远将Juliana视为自己的家人并时常去看望她。<br>
Medina于2015年与挪威女友Skylstad交往，2017年7月Skylstad生下了他们的女儿Mila。</p>
